public enum PowerUpType
{
    ExpandPaddle,    // Paddle se hace más grande
    ShrinkPaddle,    // Paddle se hace más pequeño
    MultiBall,       // Genera pelotas adicionales
    SplitBall,       // La pelota se divide al primer impacto
    SpeedUp,         // Aumenta velocidad de la pelota
    SlowDown         // Reduce velocidad de la pelota
}
